    init() {
        this.galleryGrid = document.querySelector('.gallery-grid');
        this.setupFilters();
        this.loadImages();
        this.setupLightbox();
    }

    setupFilters() {
        const filterButtons = document.querySelectorAll('.filter-btn');
        filterButtons.forEach(btn => {
            btn.addEventListener('click', () => {
                // Remove active class from all buttons
                filterButtons.forEach(b => b.classList.remove('active'));
                // Add active class to clicked button
                btn.classList.add('active');
                
                this.currentFilter = btn.dataset.filter;
                this.loadImages();
            });
        });
    }

    loadImages() {
        this.showLoading();
        this.galleryGrid.innerHTML = ''; // Clear existing images

        const filteredImages = this.currentFilter === 'all' 
            ? this.images 
            : this.images.filter(img => img.category === this.currentFilter);

        filteredImages.forEach(image => {
            const item = this.createGalleryItem(image);
            this.galleryGrid.appendChild(item);
        });

        this.hideLoading();
    }

    createGalleryItem(image) {
        const div = document.createElement('div');
        div.className = 'gallery-item';
        div.innerHTML = `
            <a href="${image.src}" data-lightbox="wedding-gallery" data-title="${image.title}">
                <img src="${image.src}" alt="${image.title}">
                <div class="gallery-item-overlay">
                    <h3>${image.title}</h3>
                </div>
            </a>
        `;
        return div;
    }

